                     <h4 class="f24 fw6">How to Check National Permit Status for your Vehicle</h4>
                     <p>Once you have submitted your documents and made the payment to obtain your national permit, you may have to wait a few days before you get the national permit details for your vehicle. In order to perform a national permit online status check, you can simply follow the below steps:
      </p>
      <p>
         <strong>Step 1:</strong> Log on to the Parivahan Sewa portal<br/>
         <strong>Step 2:</strong> Under the ‘Online Services’ section on the menu bar, select 'National Permit'<br/>
         <strong>Step 3:</strong> On the new page where you will be redirected to, enter your vehicle’s registration number and the last five characters of your chassis number.
      </p>
      <p>With this, you can check national permit status for your vehicle easily. You can also do an online national permit verification for your vehicle to confirm the details. Along with that, the Parivahan Sewa portal allows you to check and verify the status of your national permit online payment by providing the above information.</p>
