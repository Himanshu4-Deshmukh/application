                     <h4 class="f24 fw6">Rules and Conditions to Get a National Permit in India</h4>
                     <p>Before you get a national permit for your vehicle, you must check national permit rules in the country. The regulations for national permit for cars, or national permit for two wheelers are different from passenger vehicles moving interstate or national permit for trucks rules. According to the national permit system, the rules to follow and conditions to be met to get a national permit are:
                  </p>
                  <ul>
                    <li>According to rule 86 and 87 of Central Motor Vehicles Rules, 1989, a national India permit is issued for a minimum of four continuous states, which includes the home state. </li>
                    <li>The age of a vehicle to be eligible for a national permit should not exceed 12 years. In case of multi-axle vehicles, the maximum age to get a national permit is 15 years.</li>
                    <li>Form 46 and Form 48 are mandatory to apply for a vahan national permit.</li>
                    <li>A national permit for a motor cab expires after 9 years from the date of issuance. For vehicles other than a motor cab, the national permit expires after 8 years.</li>
                    <li>In case of renewal of national permit, an application along with the permit renewal fee is required to be submitted 15 days prior to the date of expiry of the permit.</li>
                    <li>In case of replacement of the vehicle, the permit holder, after taking the due permission from the issuing authority, can replace the vehicle covered by the permit with another vehicle of the same category, for which a period of 4 months is allowed. </li>
                  </ul>
