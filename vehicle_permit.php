                     <h4 class="f24 fw6">National Permit Online Application Process</h4>
                     <p>Generally, for all commercial transportation purposes, you need to apply for a vahan national permit to legally carry out commercial activities in the country. It is important to remember that the national permit validity in India is for 9 years for a motor cab, and for 8 years for other types of motor vehicles.
      </p>
      <p>Before applying for a national permit, you need to have the following documents in place:</p>
      <ul>
        <li>Original registration certificate of the vehicle</li>
        <li>Vehicle’s fitness certificate</li>
        <li>Insurance certificate of the vehicle</li>
        <li>Proof of tax payment to the Home state in the current quarter</li>
        <li>Form 46</li>
        <li>Form 48</li>
        <li>Fee for national permit duly paid</li>
        <li>Demand drafts, made in favour of transport authorities, agreed in respect of other states towards payment of taxes</li>
        <li>Proof of green tax payment (wherever applicable)</li>
      </ul>
      <p>
       With the above documents, in order to obtain an online national permit, you need to visit your respective Regional Transport Office (RTO). Once you submit the application for the national permit, along with the documents mentioned above and the authorisation national permit fee, the data granting you the national permit will be updated on the Ministry of Road Transport and Highways (MoRTH)’s Parivahan Sewa portal. After the payment is made, your zonal RTO will hand over the Parivahan National Permit to you.</p>
