                     <h4 class="f24 fw6">Step-By-Step Process For Online Payment of National Permit</h4>
                     <p>To have a national permit issued to your vehicle, you are required to pay a requisite amount as fee. Once you have applied for a national permit, a common question that arises later is how to pay national permit fee online. In this digital era, the Ministry of Road Transport and Highways has provided a simple solution to this question via its Parivahan Sewa portal.
                  </p>
                  <p>To make your national permit online payment, you need to follow the steps mentioned below:</p>
                  <p>
                    <strong>Step 1:</strong> Log on to the Parivahan Sewa portal.<br/>
                    <strong>Step 2:</strong> Under the ‘Online Services’ section on the menu bar, select ‘National Permit’.<br/>
                    <strong>Step 3:</strong> Under the ‘Payment and Transaction Status’ section, click on ‘Online Payment of National Permit’.<br/>
                    <strong>Step 4:</strong> You will then be redirected to a new page, where you will be prompted to enter your vehicle’s registration number and the last five characters of your chassis number.<br/>
                    <strong>Step 5:</strong> You can then make the composite fee payment via card or net banking.
                  </p>
                  <p>The national permit authorisation for operating throughout the territory of India is granted to you after you have made the national permit payment.</p>
